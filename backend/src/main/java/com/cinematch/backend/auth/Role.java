// Δηλώνουμε σε ποιο πακέτο (package) ανήκει η κλάση/enum.
// Το αλλάζεις ώστε να ταιριάζει με τη δομή του project σας.
package com.cinematch.backend.auth;

// Το παρακάτω Javadoc σχόλιο περιγράφει τι κάνει το enum Role.
// Δεν είναι υποχρεωτικό για να "τρέχει" ο κώδικας, αλλά βοηθάει στην τεκμηρίωση.
/**
 * Το Role enum ορίζει τους διαφορετικούς ρόλους χρήστη στο σύστημα CineMatch.
 * Οι ρόλοι αυτοί θα χρησιμοποιηθούν αργότερα για έλεγχο δικαιωμάτων (authorization).
 */
public enum Role {   // Δηλώνουμε ένα public enum με όνομα Role.

    // Κάθε γραμμή από κάτω είναι μία σταθερά (constant) του enum.

    // GUEST: Χρήστης που δεν έχει κάνει login ή έχει πολύ περιορισμένα δικαιώματα.
    GUEST,

    // USER: Κανονικός χρήστης που έχει κάνει login και έχει τυπικά δικαιώματα.
    USER,

    // ADMIN: Διαχειριστής του συστήματος με αυξημένα δικαιώματα.
    ADMIN
    // Δεν χρειάζονται ελληνικά γράμματα/τόνοι στα ονόματα των enum values.
    // Μελλοντικά μπορούμε να προσθέσουμε methods μέσα στο enum αν χρειαστεί.
}

